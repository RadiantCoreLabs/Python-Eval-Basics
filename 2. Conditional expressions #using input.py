Your_Weight = input("Please enter your weight in KG:")

Your_Weight = int(Your_Weight)

if (Your_Weight > 110) :


    print("Your weight is too high")

elif (Your_Weight < 90) :


    print("Your  weight is standard")

elif (Your_Weight < 65) :


    print("Your weight is too low")

elif (Your_Weight < 100) :

    print("Your weight is fine")


elif (Your_Weight == 110) :


    print("Your weight is almost fine but close to limit comparing to your height")

else :


    print("Your weight is fine")