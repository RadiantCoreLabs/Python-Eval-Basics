Your_speed = input("Please enter your speed in km:")

Your_speed = int(Your_speed)

if (Your_speed > 120) :


    print("Your speed is too high")

elif (Your_speed < 80) :


    print("Your speed is too low")

elif (Your_speed == 120) :


    print("Your speed is fine but close to speed limit")

else :


    print("Your speed is fine")